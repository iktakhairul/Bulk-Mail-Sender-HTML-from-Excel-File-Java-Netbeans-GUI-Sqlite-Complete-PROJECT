    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed

        // TODO add your handling code here:
        String sql = "insert into Account(ID,PIN,Name,Gmail,Password)values(?,?,?,?,?)";
        try{
            pst=conn.prepareStatement(sql);
            
            pst.setString(1, jTextField3.getText());//id
            pst.setString(2, jTextField4.getText());//pin
            pst.setString(3, jTextField6.getText());//name
            pst.setString(4, jTextField9.getText());//Gmail
            pst.setString(5, jTextField8.getText());//Password
            String Show = jTextField6.getText();
            pst.execute();
            
            jTextField7.setText(Show+" Account has been Created");
            
            
        }catch(Exception e){
           JOptionPane.showMessageDialog(null, "Creation Problem");
           
        }
        
    }//GEN-LAST:event_jButton3ActionPerformed

